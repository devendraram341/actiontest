package com.attunedlabs.leapentity.config.impl;

public class LeapEntityConfigParserException extends Exception{
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 2479557273659967015L;

	public LeapEntityConfigParserException() {
		super();
	}

	public LeapEntityConfigParserException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public LeapEntityConfigParserException(String message, Throwable cause) {
		super(message, cause);
	
	}

	public LeapEntityConfigParserException(String message) {
		super(message);
	
	}

	public LeapEntityConfigParserException(Throwable cause) {
		super(cause);
		
	}

}
