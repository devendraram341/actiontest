package com.leap.authorization.exception;

public class ResourceRemovalException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 4763119560646494449L;

	public ResourceRemovalException() {
	}

	public ResourceRemovalException(String arg0) {
		super(arg0);
	}

	public ResourceRemovalException(Throwable arg0) {
		super(arg0);
	}

	public ResourceRemovalException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ResourceRemovalException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
