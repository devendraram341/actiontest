package com.leap.authentication.exception;

public class CompanyIdentificationException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -8467969950267747027L;

	public CompanyIdentificationException() {
	}

	public CompanyIdentificationException(String arg0) {
		super(arg0);
	}

	public CompanyIdentificationException(Throwable arg0) {
		super(arg0);
	}

	public CompanyIdentificationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CompanyIdentificationException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
