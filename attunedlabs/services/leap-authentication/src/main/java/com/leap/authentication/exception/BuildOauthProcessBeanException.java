package com.leap.authentication.exception;

public class BuildOauthProcessBeanException extends Exception {
	private static final long serialVersionUID = 1L;

	public BuildOauthProcessBeanException() {
		super();
	}

	public BuildOauthProcessBeanException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public BuildOauthProcessBeanException(String message, Throwable cause) {
		super(message, cause);
	}

	public BuildOauthProcessBeanException(String message) {
		super(message);
	}

	public BuildOauthProcessBeanException(Throwable cause) {
		super(cause);
	}

}
