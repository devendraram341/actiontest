package com.leap.authentication.exception;

public class TenantIdentificationException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -7677296588344973021L;

	public TenantIdentificationException() {
	}

	public TenantIdentificationException(String arg0) {
		super(arg0);
	}

	public TenantIdentificationException(Throwable arg0) {
		super(arg0);
	}

	public TenantIdentificationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public TenantIdentificationException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
